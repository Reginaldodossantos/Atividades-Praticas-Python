# Altura da caixa (em cm)
altura = 20

# -------------------------------
# Cálculo do volume
# -------------------------------

# Aplicação da fórmula do volume
volume = comprimento * largura * altura

# -------------------------------
# Exibição dos resultados
# -------------------------------

print("Calculadora de Volume - Caixa Retangular")
print("----------------------------------------")
print(f"Comprimento: {comprimento} cm")
